	public static TriggerType lookup(int type) {
		if (type < 0) {
			return TriggerType.USER_NAMED;
		}
		for (TriggerType t : TriggerType.values()) {
			if (t.type == type) {
				return t;
			}
		}
		throw new IllegalArgumentException("Unknown TriggerType 0x" + Integer.toHexString(type));
	}

